% Converts hyperfine couplings from Gauss to MHz (linear frequency).
%
% ilya.kuprov@oerc.ox.ac.uk

function hfc_mhz=gauss2mhz(hfc_gauss)

    hfc_mhz=2.802495365*hfc_gauss;
    
end

